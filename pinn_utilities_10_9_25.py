def plot_loss(epochs, trainer, save_path=None):
    """
    Plots the complete training loss history over epochs.

    Args:
        epochs (int): The total number of training epochs.
        trainer (PINNTrainer): The trainer object containing loss histories.
        save_path (str, optional): If provided, saves the plot to this path.
                                   Otherwise, shows the plot. Defaults to None.
    """
    plt.figure(figsize=(12, 6))
    plt.plot(range(epochs), trainer.loss_history, label='Total Loss')
    plt.plot(range(epochs), trainer.data_loss_history, label='Data (MSE) Loss', linestyle=':')
    plt.plot(range(epochs), trainer.pde_loss_history, label='P(t) PDE Loss', linestyle=':')
    for name, history in trainer.temporal_losses_history.items():
        plt.plot(range(epochs), history, label=f'{name} PDE Loss', linestyle=':')
    plt.xlabel('Epoch')
    plt.ylabel('Loss')
    plt.title('Training Loss Over Time')
    plt.yscale('log')
    plt.legend()
    plt.grid(True)

    if save_path:
        plt.savefig(save_path, dpi=300, bbox_inches='tight')
        plt.close() # Close the plot to free up memory and prevent display
    else:
        plt.show()