import matplotlib.pyplot as plt

def create_strided_kernel(size, stride):
    """
    Creates a 1D PyTorch convolution kernel for a strided moving average.
    """
    kernel_tensor = torch.zeros(size)
    kernel_tensor[::stride] = 1
    return (kernel_tensor / kernel_tensor.sum()).view(1, 1, -1)

def create_gaussian_kernel(size, std):
    """
    Creates a 1D PyTorch convolution kernel with Gaussian weights.
    """
    t = np.linspace(-size // 2, size // 2, size)
    gauss = np.exp(-0.5 * (t / std) ** 2)
    kernel = torch.tensor(gauss, dtype=torch.float32)
    return (kernel / kernel.sum()).view(1, 1, -1)

